package com.example.carinformation;

public class Car {
    private String model;
    private String brand;
    private int year;
    private float price;
    private String color;
    private int quantity;

    public Car(String m, String b, int y, float p, String c, int q) {
        model = m;
        brand = b;
        year = y;
        price = p;
        color = c;
        quantity = q;
    }
}
